#include "../include/pipex.h"

void	ft_free_matrix(char ***cmd)
{
	int	i;

	i = 0;
	while (cmd[0][i])
	{
		free(cmd[0][i]);
		i++;
	}
	free(cmd[0]);
	cmd = NULL;
}
